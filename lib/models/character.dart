class Character {
  final String imageUrl;
  final String name;
  final double stars;
  final int reviews;
  final int strength;
  final int magic;
  final int speed;

  Character({
    required this.imageUrl,
    required this.name,
    required this.stars,
    required this.reviews,
    required this.strength,
    required this.magic,
    required this.speed,
  });
}
